let a = 10;
console.log(a > 10);   // a es mayor que 10 
console.log(a >= 10);  // a es mayor o igual a 10 
console.log(a < 10);   // a es menor que 10 
console.log(a <= 10);  // a es menor o igual a 10 

console.log(a == 10); // -> a es igual a 10
console.log(a != 10); // -> a es distinto a 10
console.log(a === '10'); // -> a es igual a '10' y ambos son del mismo tipo (number y string)
console.log(a !== '10'); // -> a es distinto a '10' o son de diferente tipo
 